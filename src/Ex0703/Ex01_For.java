package Ex0703;

public class Ex01_For {

	public static void main(String[] args) {
		// for문
		//1부터 3까지 출력하기
		//
		int i = 0;

		i = 0;
		
		System.out.println("for문 시작");
		for (i=3; i<=100; i++) {
			System.out.println("for문 내부");
			System.out.println(i);
		}
		System.out.println("for문 종료");
		
		
		
	}

}
