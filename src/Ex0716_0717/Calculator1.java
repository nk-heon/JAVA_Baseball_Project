package Ex0716_0717;

public class Calculator1 {
	 
	
	int sum(int x,int y) {
		
		int result = x + y;
		return result;
	}
	
	int sub(int x,int y) {
		
		int result = x - y;
		return result;
	}
	
	int mul(int x, int y) {
		
		int result = x * y;
		return result;
	}
	
	int div(int x, int y) {
		int result = x / y;
		return result;
	}
	

}
